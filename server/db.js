// Capa de datos dual:
//  - Si DATABASE_URL está definida (Supabase/Postgres) → usa pg.
//  - Si no → SQLite local (agenda.db), comportamiento original.
// API: initDb(), dbAll(sql, ...p), dbGet(sql, ...p), dbRun(sql, ...p)
// Los placeholders se escriben SIEMPRE con '?' (estilo SQLite) y se
// convierten a $1..$n automáticamente en el backend Postgres.
// Las fechas se guardan como TEXT (YYYY-MM-DD) en ambos backends para
// mantener idéntica la semántica de BETWEEN y comparaciones.

import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));

export const usingPostgres = !!process.env.DATABASE_URL;

let dbAll, dbGet, dbRun, initDb;

// ============================================================
// Backend Postgres (Supabase)
// ============================================================
if (usingPostgres) {
  const { default: pg } = await import('pg');
  const pool = new pg.Pool({
    connectionString: process.env.DATABASE_URL,
    ssl: { rejectUnauthorized: false },
    max: 5,
  });

  const toDollar = (sql) => {
    let i = 0;
    return sql.replace(/\?/g, () => `$${++i}`);
  };

  const query = (sql, params) => pool.query(toDollar(sql), params);

  dbAll = async (sql, ...params) => (await query(sql, params)).rows;
  dbGet = async (sql, ...params) => (await query(sql, params)).rows[0];
  dbRun = async (sql, ...params) => {
    const r = await query(sql, params);
    return { changes: r.rowCount, lastInsertRowid: r.rows?.[0]?.id };
  };

  const SCHEMA_PG = `
    CREATE TABLE IF NOT EXISTS projects (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      name TEXT NOT NULL UNIQUE,
      color TEXT NOT NULL DEFAULT '#4f8cff',
      expected_hours DOUBLE PRECISION NOT NULL DEFAULT 0,
      description TEXT NOT NULL DEFAULT '',
      links TEXT NOT NULL DEFAULT '',
      created_at TEXT NOT NULL DEFAULT to_char(now(), 'YYYY-MM-DD HH24:MI:SS')
    );

    CREATE TABLE IF NOT EXISTS work_entries (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      date TEXT NOT NULL,
      start_time TEXT NOT NULL DEFAULT '',
      end_time TEXT NOT NULL DEFAULT '',
      hours DOUBLE PRECISION NOT NULL DEFAULT 0,
      project_id INTEGER REFERENCES projects(id) ON DELETE SET NULL,
      note TEXT
    );

    CREATE TABLE IF NOT EXISTS todos (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      date TEXT,
      text TEXT NOT NULL,
      done INTEGER NOT NULL DEFAULT 0,
      sort_order INTEGER NOT NULL DEFAULT 0,
      parent_id INTEGER REFERENCES todos(id) ON DELETE SET NULL
    );

    CREATE TABLE IF NOT EXISTS events (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      date TEXT NOT NULL,
      title TEXT NOT NULL,
      kind TEXT NOT NULL DEFAULT 'event'
    );

    CREATE TABLE IF NOT EXISTS labor_days (
      date TEXT PRIMARY KEY,
      type TEXT NOT NULL,
      label TEXT
    );

    CREATE TABLE IF NOT EXISTS settings (
      key TEXT PRIMARY KEY,
      value TEXT NOT NULL
    );

    CREATE TABLE IF NOT EXISTS project_month_hours (
      project_id INTEGER NOT NULL REFERENCES projects(id) ON DELETE CASCADE,
      month TEXT NOT NULL,
      hours DOUBLE PRECISION NOT NULL DEFAULT 0,
      PRIMARY KEY (project_id, month)
    );

    CREATE TABLE IF NOT EXISTS outlook_meetings_cache (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      uid TEXT NOT NULL,
      date TEXT NOT NULL,
      title TEXT NOT NULL,
      start_time TEXT,
      end_time TEXT,
      all_day INTEGER NOT NULL DEFAULT 0,
      teams_url TEXT,
      cached_at TEXT NOT NULL DEFAULT to_char(now(), 'YYYY-MM-DD HH24:MI:SS'),
      UNIQUE(uid, date)
    );

    CREATE TABLE IF NOT EXISTS meeting_projects (
      uid TEXT NOT NULL,
      date TEXT NOT NULL,
      project_id INTEGER NOT NULL REFERENCES projects(id) ON DELETE CASCADE,
      PRIMARY KEY (uid, date)
    );

    CREATE TABLE IF NOT EXISTS custom_meetings (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      date TEXT NOT NULL,
      title TEXT NOT NULL,
      start_time TEXT,
      end_time TEXT,
      all_day INTEGER NOT NULL DEFAULT 0,
      notes TEXT NOT NULL DEFAULT '',
      links TEXT NOT NULL DEFAULT '',
      repeat TEXT NOT NULL DEFAULT 'none',
      repeat_until TEXT,
      created_at TEXT NOT NULL DEFAULT to_char(now(), 'YYYY-MM-DD HH24:MI:SS')
    );

    CREATE TABLE IF NOT EXISTS meeting_notes (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      meeting_type TEXT NOT NULL,
      meeting_ref TEXT NOT NULL,
      meeting_date TEXT NOT NULL,
      notes TEXT NOT NULL DEFAULT '',
      links TEXT NOT NULL DEFAULT '',
      UNIQUE(meeting_type, meeting_ref, meeting_date)
    );

    CREATE TABLE IF NOT EXISTS meeting_attendance (
      uid TEXT NOT NULL,
      date TEXT NOT NULL,
      attending INTEGER NOT NULL DEFAULT 1,
      PRIMARY KEY (uid, date)
    );

    CREATE TABLE IF NOT EXISTS chat_conversations (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      title TEXT NOT NULL DEFAULT 'Nueva conversación',
      created_at TEXT NOT NULL DEFAULT to_char(now(), 'YYYY-MM-DD HH24:MI:SS'),
      updated_at TEXT NOT NULL DEFAULT to_char(now(), 'YYYY-MM-DD HH24:MI:SS')
    );

    CREATE TABLE IF NOT EXISTS chat_messages (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      conversation_id INTEGER NOT NULL REFERENCES chat_conversations(id) ON DELETE CASCADE,
      role TEXT NOT NULL,
      content TEXT NOT NULL,
      created_at TEXT NOT NULL DEFAULT to_char(now(), 'YYYY-MM-DD HH24:MI:SS')
    );

    CREATE TABLE IF NOT EXISTS ai_snapshots (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      snapshot_date TEXT NOT NULL UNIQUE,
      data TEXT NOT NULL,
      created_at TEXT NOT NULL DEFAULT to_char(now(), 'YYYY-MM-DD HH24:MI:SS')
    );
  `;

  initDb = async () => {
    await pool.query(SCHEMA_PG);
    const has = await dbGet('SELECT 1 AS ok FROM settings WHERE key = ?', 'vacation_total');
    if (!has) await dbRun('INSERT INTO settings(key, value) VALUES(?, ?)', 'vacation_total', '23');
    console.log('[db] Backend: Postgres (Supabase)');
  };

// ============================================================
// Backend SQLite (agenda.db) — comportamiento original
// ============================================================
} else {
  const { default: Database } = await import('better-sqlite3');
  const db = new Database(path.join(__dirname, 'agenda.db'));
  db.pragma('journal_mode = WAL');
  db.pragma('foreign_keys = ON');

  db.exec(`
    CREATE TABLE IF NOT EXISTS projects (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      name TEXT NOT NULL UNIQUE,
      color TEXT NOT NULL DEFAULT '#4f8cff',
      expected_hours REAL NOT NULL DEFAULT 0,
      created_at TEXT NOT NULL DEFAULT (datetime('now'))
    );

    CREATE TABLE IF NOT EXISTS work_entries (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      date TEXT NOT NULL,
      start_time TEXT NOT NULL,
      end_time TEXT NOT NULL,
      project_id INTEGER,
      note TEXT,
      FOREIGN KEY(project_id) REFERENCES projects(id) ON DELETE SET NULL
    );

    CREATE TABLE IF NOT EXISTS todos (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      date TEXT,
      text TEXT NOT NULL,
      done INTEGER NOT NULL DEFAULT 0,
      sort_order INTEGER NOT NULL DEFAULT 0
    );

    CREATE TABLE IF NOT EXISTS events (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      date TEXT NOT NULL,
      title TEXT NOT NULL,
      kind TEXT NOT NULL DEFAULT 'event'
    );

    CREATE TABLE IF NOT EXISTS labor_days (
      date TEXT PRIMARY KEY,
      type TEXT NOT NULL,
      label TEXT
    );

    CREATE TABLE IF NOT EXISTS settings (
      key TEXT PRIMARY KEY,
      value TEXT NOT NULL
    );

    CREATE TABLE IF NOT EXISTS project_month_hours (
      project_id INTEGER NOT NULL,
      month TEXT NOT NULL,
      hours REAL NOT NULL DEFAULT 0,
      PRIMARY KEY (project_id, month),
      FOREIGN KEY(project_id) REFERENCES projects(id) ON DELETE CASCADE
    );
  `);

  // Migration: add parent_id to todos for carry-over (multi-day tasks)
  try {
    const cols = db.prepare("PRAGMA table_info(todos)").all();
    if (!cols.find(c => c.name === 'parent_id')) {
      db.exec('ALTER TABLE todos ADD COLUMN parent_id INTEGER REFERENCES todos(id) ON DELETE SET NULL');
    }
  } catch (_) { /* ignore */ }

  // Migration: allow NULL date in todos for unassigned tasks
  try {
    const cols = db.prepare("PRAGMA table_info(todos)").all();
    const dateCol = cols.find(c => c.name === 'date');
    if (dateCol && dateCol.notnull === 1) {
      db.exec(`
        CREATE TABLE todos_new (
          id INTEGER PRIMARY KEY AUTOINCREMENT,
          date TEXT,
          text TEXT NOT NULL,
          done INTEGER NOT NULL DEFAULT 0,
          sort_order INTEGER NOT NULL DEFAULT 0
        );
        INSERT INTO todos_new(id, date, text, done, sort_order)
        SELECT id, date, text, done, id FROM todos;
        DROP TABLE todos;
        ALTER TABLE todos_new RENAME TO todos;
      `);
    }
  } catch (e) {
    console.error('Migration error:', e.message);
  }

  try {
    const todoCols = db.prepare("PRAGMA table_info(todos)").all();
    if (!todoCols.find(c => c.name === 'sort_order')) {
      db.exec('ALTER TABLE todos ADD COLUMN sort_order INTEGER NOT NULL DEFAULT 0');
    }
    db.exec(`
      WITH ranked AS (
        SELECT id, ROW_NUMBER() OVER (PARTITION BY COALESCE(date, '__unassigned__') ORDER BY sort_order, id) AS rn
        FROM todos
      )
      UPDATE todos
      SET sort_order = (SELECT rn FROM ranked WHERE ranked.id = todos.id)
      WHERE id IN (SELECT id FROM ranked)
    `);
  } catch (_) { /* ignore */ }

  // ajustes por defecto
  const hasSetting = db.prepare('SELECT 1 FROM settings WHERE key = ?');
  const setSetting = db.prepare('INSERT INTO settings(key, value) VALUES(?, ?)');
  if (!hasSetting.get('vacation_total')) setSetting.run('vacation_total', '23');

  // migración suave: añade expected_hours si el schema es antiguo
  try {
    const cols = db.prepare("PRAGMA table_info(projects)").all();
    if (!cols.find(c => c.name === 'expected_hours')) {
      db.exec('ALTER TABLE projects ADD COLUMN expected_hours REAL NOT NULL DEFAULT 0');
    }
  } catch (_) { /* ignore */ }

  // migración: añade description y links a projects
  try {
    const pCols = db.prepare("PRAGMA table_info(projects)").all();
    if (!pCols.find(c => c.name === 'description')) {
      db.exec("ALTER TABLE projects ADD COLUMN description TEXT NOT NULL DEFAULT ''");
    }
    if (!pCols.find(c => c.name === 'links')) {
      db.exec("ALTER TABLE projects ADD COLUMN links TEXT NOT NULL DEFAULT ''");
    }
  } catch (_) { /* ignore */ }

  // migración: añade columna hours a work_entries
  try {
    const wCols = db.prepare("PRAGMA table_info(work_entries)").all();
    if (!wCols.find(c => c.name === 'hours')) {
      db.exec('ALTER TABLE work_entries ADD COLUMN hours REAL NOT NULL DEFAULT 0');
      db.exec(`UPDATE work_entries SET hours = ROUND(
        ((CAST(substr(end_time,1,2) AS REAL)*60 + CAST(substr(end_time,4,2) AS REAL)) -
         (CAST(substr(start_time,1,2) AS REAL)*60 + CAST(substr(start_time,4,2) AS REAL))) / 60.0, 2
      ) WHERE hours = 0 AND start_time IS NOT NULL AND end_time IS NOT NULL`);
    }
  } catch (_) { /* ignore */ }

  db.exec(`
    CREATE TABLE IF NOT EXISTS outlook_meetings_cache (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      uid TEXT NOT NULL,
      date TEXT NOT NULL,
      title TEXT NOT NULL,
      start_time TEXT,
      end_time TEXT,
      all_day INTEGER NOT NULL DEFAULT 0,
      teams_url TEXT,
      cached_at TEXT NOT NULL DEFAULT (datetime('now')),
      UNIQUE(uid, date)
    );

    CREATE TABLE IF NOT EXISTS meeting_projects (
      uid TEXT NOT NULL,
      date TEXT NOT NULL,
      project_id INTEGER NOT NULL,
      PRIMARY KEY (uid, date),
      FOREIGN KEY(project_id) REFERENCES projects(id) ON DELETE CASCADE
    );

    CREATE TABLE IF NOT EXISTS custom_meetings (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      date TEXT NOT NULL,
      title TEXT NOT NULL,
      start_time TEXT,
      end_time TEXT,
      all_day INTEGER NOT NULL DEFAULT 0,
      notes TEXT NOT NULL DEFAULT '',
      links TEXT NOT NULL DEFAULT '',
      created_at TEXT NOT NULL DEFAULT (datetime('now'))
    );
  `);

  // migración: recurrencia semanal en custom_meetings
  try {
    const cmCols = db.prepare("PRAGMA table_info(custom_meetings)").all();
    if (!cmCols.find(c => c.name === 'repeat')) {
      db.exec("ALTER TABLE custom_meetings ADD COLUMN repeat TEXT NOT NULL DEFAULT 'none'");
    }
    if (!cmCols.find(c => c.name === 'repeat_until')) {
      db.exec('ALTER TABLE custom_meetings ADD COLUMN repeat_until TEXT');
    }
  } catch (_) { /* ignore */ }

  db.exec(`
    CREATE TABLE IF NOT EXISTS meeting_notes (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      meeting_type TEXT NOT NULL,
      meeting_ref TEXT NOT NULL,
      meeting_date TEXT NOT NULL,
      notes TEXT NOT NULL DEFAULT '',
      links TEXT NOT NULL DEFAULT '',
      UNIQUE(meeting_type, meeting_ref, meeting_date)
    );

    CREATE TABLE IF NOT EXISTS meeting_attendance (
      uid TEXT NOT NULL,
      date TEXT NOT NULL,
      attending INTEGER NOT NULL DEFAULT 1,
      PRIMARY KEY (uid, date)
    );

    CREATE TABLE IF NOT EXISTS chat_conversations (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      title TEXT NOT NULL DEFAULT 'Nueva conversación',
      created_at TEXT NOT NULL DEFAULT (datetime('now')),
      updated_at TEXT NOT NULL DEFAULT (datetime('now'))
    );

    CREATE TABLE IF NOT EXISTS chat_messages (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      conversation_id INTEGER NOT NULL,
      role TEXT NOT NULL,
      content TEXT NOT NULL,
      created_at TEXT NOT NULL DEFAULT (datetime('now')),
      FOREIGN KEY(conversation_id) REFERENCES chat_conversations(id) ON DELETE CASCADE
    );

    CREATE TABLE IF NOT EXISTS ai_snapshots (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      snapshot_date TEXT NOT NULL UNIQUE,
      data TEXT NOT NULL,
      created_at TEXT NOT NULL DEFAULT (datetime('now'))
    );
  `);

  // SQLite no acepta booleanos como parámetros: normalizar a 0/1/null
  const norm = (params) => params.map(p => (typeof p === 'boolean' ? (p ? 1 : 0) : p));

  dbAll = async (sql, ...params) => db.prepare(sql).all(...norm(params));
  dbGet = async (sql, ...params) => db.prepare(sql).get(...norm(params));
  dbRun = async (sql, ...params) => {
    if (/\breturning\b/i.test(sql)) {
      const row = db.prepare(sql).get(...norm(params));
      return { changes: 1, lastInsertRowid: row?.id };
    }
    const info = db.prepare(sql).run(...norm(params));
    return { changes: info.changes, lastInsertRowid: info.lastInsertRowid };
  };

  initDb = async () => {
    console.log('[db] Backend: SQLite local (agenda.db)');
  };
}

export { dbAll, dbGet, dbRun, initDb };

// Compatibilidad con módulos antiguos (ai.js, desactivado) que hacían
// `import db from './db.js'` y usaban la API síncrona de better-sqlite3.
// En modo Postgres ese módulo no debe usarse sin adaptarlo antes.
export default null;
